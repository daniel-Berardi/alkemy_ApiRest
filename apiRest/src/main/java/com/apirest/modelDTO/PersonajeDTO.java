package com.apirest.modelDTO;

public class PersonajeDTO {
	
	private String nombre;
	
	private Byte[] imagen;
	
	
	

	public PersonajeDTO() {
		super();
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public Byte[] getImagen() {
		return imagen;
	}

	public void setImagen(Byte[] imagen) {
		this.imagen = imagen;
	}

	
	
	
	
	
}
